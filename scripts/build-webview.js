#!/usr/bin/env node
/**
 * waveformRenderer.ts のコンパイル出力 (CommonJS) を webview の <script src>
 * から直接読める IIFE に変換し、dist/webview/comparisonWaveform.js を生成する。
 *
 * 元の media/comparisonWaveform.js (手書きミラー) を置き換える目的の単一ソース化。
 * 依存追加なし。 tsc 後に scripts/verify.sh と npm run compile から呼ばれる。
 */

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '..');
const SRC = path.join(ROOT, 'dist', 'webview', 'waveform', 'waveformRenderer.js');
const OUT_DIR = path.join(ROOT, 'dist', 'webview');
const OUT = path.join(OUT_DIR, 'comparisonWaveform.js');

// window 経由で webview の renderScript IIFE から呼ばれる関数の一覧。
// 新しい webview 公開関数を追加したらここに足す。
const EXPORTED_TO_WINDOW = [
    'renderWaveformPipeline',
    'paintLoopRegion',
];

if (!fs.existsSync(SRC)) {
    console.error(`build-webview: source not found: ${SRC}`);
    console.error('Run `npm run compile` (tsc) first so this script has CJS input.');
    process.exit(1);
}

const cjs = fs.readFileSync(SRC, 'utf8');
const windowAssignments = EXPORTED_TO_WINDOW
    .map((name) => `        if (typeof exports.${name} === 'function') { window.${name} = exports.${name}; }`)
    .join('\n');

const wrapped = `// AUTO-GENERATED by scripts/build-webview.js — do not edit by hand.
// Source: src/webview/waveform/waveformRenderer.ts
(function () {
    'use strict';
    const exports = {};
${cjs}
${windowAssignments}
})();
`;

if (!fs.existsSync(OUT_DIR)) {
    fs.mkdirSync(OUT_DIR, { recursive: true });
}
fs.writeFileSync(OUT, wrapped);
console.log(`build-webview: wrote ${path.relative(ROOT, OUT)} (${wrapped.length} bytes)`);
